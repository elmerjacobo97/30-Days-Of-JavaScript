// trim(): Removes trailing space in the beginning or the end of a string.
let string = "   30 Days Of JavaScript   ";
console.log(string); //
console.log(string.trim()); // Inicio y final
console.log(string.trimStart()); // Inicio
console.log(string.trimEnd()); // Final

